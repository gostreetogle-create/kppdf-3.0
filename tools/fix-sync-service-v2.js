const fs = require('fs');
const path = require('path');

const filePath = path.resolve(__dirname, '..', '..', 'yougile-sync-server', 'src', 'services', 'yougile-sync.service.ts');

const content = [
  "import { yougileApi } from './yougile-api.service';",
  "import { logger } from './logger.service';",
  "import { sheetsService } from './google-sheets.service';",
  "import type { YouGileTask, ModuleTaskMapping, SyncResult, SyncDetail, ReadinessSnapshot, DailySnapshot } from '../types';",
  "import { loadMapping } from '../config/mapping';",
  "import { v4 as uuid } from 'uuid';",
  "",
  "const MAX_RETRY_QUEUE = 50;",
  "",
  "/**",
  " * YouGile Sync Service - synchronization of implementation status",
  " */",
  "export class YouGileSyncService {",
  "  private lastSyncAt: Date | null = null;",
  "  private retryQueue: Array<{ entry: ModuleTaskMapping; retryCount: number; lastError: string }> = [];",
  "  private lastDailySnapshotDate: string = '';",
  "",
  "  async syncAll(dryRun = false): Promise<SyncResult> {",
  "    const startTime = Date.now();",
  "    const mapping = await loadMapping();",
  "    const details: SyncDetail[] = [];",
  "    let updated = 0, skipped = 0, errors = 0;",
  "    logger.info('Starting sync (dryRun=' + dryRun + ', mappings=' + mapping.mappings.length + ')');",
  "    if (this.retryQueue.length > 0 && !dryRun) {",
  "      logger.info('Processing retry queue', { count: this.retryQueue.length });",
  "      const retryResults = await this.processRetryQueue();",
  "      details.push(...retryResults);",
  "      for (const r of retryResults) {",
  "        if (r.action === 'update') updated++;",
  "        else if (r.action === 'error') errors++;",
  "        else skipped++;",
  "      }",
  "    }",
  "    for (const entry of mapping.mappings) {",
  "      try {",
  "        const task = await yougileApi.getTask(entry.task_id);",
  "        const result = await this.syncTask(task, entry, dryRun);",
  "        if (result.action === 'update') updated++;",
  "        else if (result.action === 'error') errors++;",
  "        else skipped++;",
  "        details.push(result);",
  "      } catch (error) {",
  "        errors++;",
  "        const errMsg = String(error);",
  "        details.push({ module_key: entry.module_key, task_id: entry.task_id, action: 'error', error: errMsg });",
  "        logger.error('Sync failed for ' + entry.module_key, { error: errMsg });",
  "        if (!dryRun && this.retryQueue.length < MAX_RETRY_QUEUE) {",
  "          this.retryQueue.push({ entry, retryCount: 0, lastError: errMsg });",
  "        }",
  "      }",
  "    }",
  "    this.lastSyncAt = new Date();",
  "    const result: SyncResult = { dry_run: dryRun, timestamp: new Date().toISOString(), updated, skipped, errors, details };",
  "    if (!dryRun) {",
  "      await this.logSyncEvent(result);",
  "      await this.maybeWriteDailySnapshot(result);",
  "    }",
  "    logger.info('Sync complete', { updated, skipped, errors, duration: Date.now() - startTime, retryQueue: this.retryQueue.length });",
  "    return result;",
  "  }",
  "",
  "  private async processRetryQueue(): Promise<SyncDetail[]> {",
  "    const results: SyncDetail[] = [];",
  "    const remaining: typeof this.retryQueue = [];",
  "    for (const item of this.retryQueue) {",
  "      try {",
  "        const task = await yougileApi.getTask(item.entry.task_id);",
  "        const result = await this.syncTask(task, item.entry, false);",
  "        results.push(result);",
  "        logger.info('Retry success for ' + item.entry.module_key);",
  "      } catch (error) {",
  "        const errMsg = String(error);",
  "        const newRetryCount = item.retryCount + 1;",
  "        if (newRetryCount < 3) {",
  "          remaining.push({ ...item, retryCount: newRetryCount, lastError: errMsg });",
  "        } else {",
  "          results.push({ module_key: item.entry.module_key, task_id: item.entry.task_id, action: 'error', error: errMsg });",
  "          logger.error('Retry exhausted for ' + item.entry.module_key, { retries: newRetryCount, error: errMsg });",
  "        }",
  "      }",
  "    }",
  "    this.retryQueue = remaining;",
  "    return results;",
  "  }",
  "",
  "  private async maybeWriteDailySnapshot(result: SyncResult): Promise<void> {",
  "    const today = new Date().toISOString().slice(0, 10);",
  "    if (this.lastDailySnapshotDate === today) return;",
  "    try {",
  "      const mapping = await loadMapping();",
  "      const tasks = await yougileApi.getAllTasks();",
  "      const taskMap = new Map(tasks.map((t) => [t.id, t]));",
  "      let totalTasks = 0, doneTasks = 0;",
  "      for (const entry of mapping.mappings) {",
  "        const task = taskMap.get(entry.task_id);",
  "        if (!task) continue;",
  "        totalTasks++;",
  "        if (task.completed) doneTasks++;",
  "      }",
  "      const snapshot: DailySnapshot = { date: today, total_tasks: totalTasks, done_tasks: doneTasks, open_issues: result.errors, sync_failures: result.errors };",
  "      await sheetsService.appendDailySnapshot(snapshot);",
  "      this.lastDailySnapshotDate = today;",
  "      logger.info('Daily snapshot written', snapshot);",
  "    } catch (error) {",
  "      logger.error('Failed to write daily snapshot', { error: String(error) });",
  "    }",
  "  }",
  "",
  "  private async syncTask(task: YouGileTask, mapping: ModuleTaskMapping, dryRun: boolean): Promise<SyncDetail> {",
  "    const subtaskStatus = this.readSubtaskStatus(task, mapping);",
  "    const percent = this.calculatePercent(subtaskStatus);",
  "    const newDescription = this.buildTaskDescription(mapping.module_key, percent, subtaskStatus);",
  "    const needsUpdate = task.description !== newDescription;",
  "    if (dryRun) {",
  "      logger.debug('Dry-run: ' + mapping.module_key + ' would update', { percent, needsUpdate });",
  "      return { module_key: mapping.module_key, task_id: task.id, action: needsUpdate ? 'update' : 'noop', percent };",
  "    }",
  "    if (needsUpdate) {",
  "      await yougileApi.updateTask(task.id, { description: newDescription });",
  "      logger.info('Updated ' + mapping.module_key, { taskId: task.id, percent });",
  "      return { module_key: mapping.module_key, task_id: task.id, action: 'update', percent };",
  "    }",
  "    return { module_key: mapping.module_key, task_id: task.id, action: 'noop', percent };",
  "  }",
  "",
  "  private readSubtaskStatus(task: YouGileTask, mapping: ModuleTaskMapping): Record<string, boolean> {",
  "    const status: Record<string, boolean> = {};",
  "    for (const item of mapping.checklist_items) {",
  "      const subtask = task.subtasks?.find((st) => st.id === item.subtask_id);",
  "      status[item.checklist_id] = subtask?.completed ?? false;",
  "    }",
  "    return status;",
  "  }",
  "",
  "  calculatePercent(subtaskStatus: Record<string, boolean>): number {",
  "    const entries = Object.values(subtaskStatus);",
  "    if (entries.length === 0) return 0;",
  "    const done = entries.filter(Boolean).length;",
  "    return Math.round((done / entries.length) * 100);",
  "  }",
  "",
  "  buildTaskDescription(moduleKey: string, percent: number, subtaskStatus: Record<string, boolean>): string {",
  "    const lines: string[] = [",
  "      '\\u{1F535} **' + moduleKey + '** \\u2014 \\u0433\\u043e\\u0442\\u043e\\u0432\\u043d\\u043e\\u0441\\u0442\\u044c: ' + percent + '%',",
  "      '',",
  "      '### \\u0421\\u0442\\u0430\\u0442\\u0443\\u0441 \\u0440\\u0435\\u0430\\u043b\\u0438\\u0437\\u0430\\u0446\\u0438\\u0438',",
  "    ];",
  "    for (const [checklistId, done] of Object.entries(subtaskStatus)) {",
  "      const icon = done ? '\\u2705' : '\\u2B1C';",
  "      const statusText = done ? '\\u0432\\u044b\\u043f\\u043e\\u043b\\u043d\\u0435\\u043d\\u043e' : '\\u043d\\u0435 \\u0432\\u044b\\u043f\\u043e\\u043b\\u043d\\u0435\\u043d\\u043e';",
  "      lines.push('- ' + icon + ' **' + checklistId + '**: ' + statusText);",
  "    }",
  "    lines.push('');",
  "    lines.push('---');",
  "    lines.push('*\\u041e\\u0431\\u043d\\u043e\\u0432\\u043b\\u0435\\u043d\\u043e: ' + new Date().toISOString().slice(0, 16).replace('T', ' ') + '*');",
  "    lines.push('*\\u0418\\u0441\\u0442\\u043e\\u0447\\u043d\\u0438\\u043a: yougile-sync-server*');",
  "    return lines.join('\\\\n');",
  "  }",
  "",
  "  private async logSyncEvent(result: SyncResult): Promise<void> {",
  "    try {",
  "      for (const detail of result.details) {",
  "        const action = detail.action === 'error' ? 'error' : (detail.action === 'noop' ? 'noop' : 'update');",
  "        await sheetsService.appendEvent({",
  "          event_id: uuid(),",
  "          timestamp: result.timestamp,",
  "          source: 'poll',",
  "          module_key: detail.module_key,",
  "          checklist_id: '',",
  "          task_id: detail.task_id,",
  "          action,",
  "          before_json: null,",
  "          after_json: JSON.stringify({ percent: detail.percent }),",
  "          sync_status: detail.action === 'error' ? 'failed' : 'success',",
  "          error_message: detail.error,",
  "        });",
  "      }",
  "    } catch (error) {",
  "      logger.error('Failed to log sync event to Sheets', { error: String(error) });",
  "    }",
  "  }",
  "",
  "  getLastSyncAt(): Date | null {",
  "    return this.lastSyncAt;",
  "  }",
  "",
  "  getRetryQueueSize(): number {",
  "    return this.retryQueue.length;",
  "  }",
  "",
  "  async buildReadinessSnapshot(): Promise<ReadinessSnapshot | null> {",
  "    try {",
  "      const mapping = await loadMapping();",
  "      const tasks = await yougileApi.getAllTasks();",
  "      const taskMap = new Map(tasks.map((t) => [t.id, t]));",
  "      const items: Record<string, import('../types').ReadinessItem> = {};",
  "      for (const entry of mapping.mappings) {",
  "        const task = taskMap.get(entry.task_id);",
  "        if (!task) continue;",
  "        const subtaskStatus = this.readSubtaskStatus(task, entry);",
  "        const percent = this.calculatePercent(subtaskStatus);",
  "        items[entry.module_key] = {",
  "          label: entry.module_key,",
  "          percent,",
  "          checklist: entry.checklist_items.map((ci) => ({",
  "            id: ci.checklist_id,",
  "            label: ci.checklist_id,",
  "            done: subtaskStatus[ci.checklist_id] ?? false,",
  "          })),",
  "        };",
  "      }",
  "      return {",
  "        enabled: true,",
  "        version: 1,",
  "        updated: new Date().toISOString().slice(0, 10),",
  "        items,",
  "      };",
  "    } catch (error) {",
  "      logger.error('Failed to build readiness snapshot', { error: String(error) });",
  "      return null;",
  "    }",
  "  }",
  "}",
  "",
  "export const syncService = new YouGileSyncService();",
  "",
].join('\n');

fs.writeFileSync(filePath, content, 'utf8');
